package kalkulator;
public class Kalkulator {
    void tambah(double angkaPertama, double angkaKedua){
        double hasil = angkaPertama + angkaKedua;
        System.out.println(angkaPertama + " + " + angkaKedua + " = " + hasil);
    } 
    void kali(double angkaPertama, double angkaKedua){
        double hasil = angkaPertama * angkaKedua;
        System.out.println(angkaPertama + " * " + angkaKedua + " = " + hasil);
    } 
}
