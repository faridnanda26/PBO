package perulangan;

public class Main {
    public static void main(String[] args) {
        int j = 0;
        int k = 0;

        // for loop
        for(int i = 0; i < 5; i++){
            System.out.println("Farid Nanda Syauqi - F1D02310050");
        }
        System.out.println("=========================================");

        // while loop
        while(j < 5){
            System.out.println("Farid Nanda Syauqi - F1D02310050");
            j++;
        }
        System.out.println("=========================================");
        
        // do while loop
        do{
            System.out.println("Farid Nanda Syauqi - F1D02310050");
            k++;
        }while(k < 5);
        System.out.println("=========================================");
    }
}
